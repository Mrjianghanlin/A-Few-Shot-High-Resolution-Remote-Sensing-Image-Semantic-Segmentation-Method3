class Deeplabv3plus(nn.Module):
    def __init__(self, backbone, num_classes=3, os=16, freeze_bn=False):
        super(Deeplabv3plus, self).__init__()
        in_channels = 2048
        low_level_channels = 256
        self.backbone = backbone

        if os == 16:
            dilations = [1, 6, 12, 18]
        elif os == 8:
            dilations = [1, 12, 24, 36]
        else:
            raise NotImplementedError

        self.aspp = ASPP(dim_in=in_channels, dim_out=256, rate=16 // os)


        # ----------------------------------#
        #   浅层特征边
        # ----------------------------------#
        self.shortcut_conv = nn.Sequential(
            nn.Conv2d(low_level_channels, 48, 1),
            nn.BatchNorm2d(48),
            nn.ReLU(inplace=True)
        )

        self.cat_conv = nn.Sequential(
            nn.Conv2d(48 + 256, 256, 3, stride=1, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),
            nn.Dropout(0.5),

            nn.Conv2d(256, 256, 3, stride=1, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),

            nn.Dropout(0.1),
        )
        self.cls_conv = nn.Conv2d(256, num_classes, 1, stride=1)

        if freeze_bn:
            self._freeze_bn()

    def forward(self, input):
        H, W  =input.size(2), input.size(3)
        x, low_level_features = self.backbone(input)
        # x1 = self.aspp1(x)
        # x2 = self.aspp2(x)
        # x3 = self.aspp3(x)
        # x4 = self.aspp4(x)
        # x5 = self.global_avg_pool(x)
        # x5 = F.interpolate(x5, size=x4.size()[2:], mode='bilinear', align_corners=True)
        # x = torch.cat((x1, x2, x3, x4, x5), dim=1)
        # x = self.conv1(x)
        # x = self.bn1(x)
        # x = self.relu(x)
        # x = F.interpolate(x, size=(int(math.ceil(input.size()[-2] / 4)),
        #                            int(math.ceil(input.size()[-1] / 4))), mode='bilinear', align_corners=True)
        x = self.aspp(x)
        low_level_features = self.shortcut_conv(low_level_features)

        # -----------------------------------------#
        #   将加强特征边上采样
        #   与浅层特征堆叠后利用卷积进行特征提取
        # -----------------------------------------#
        x = F.interpolate(x, size=(low_level_features.size(2), low_level_features.size(3)), mode='bilinear',
                          align_corners=True)
        x = self.cat_conv(torch.cat((x, low_level_features), dim=1))
        x = self.cls_conv(x)
        x = F.interpolate(x, size=(H, W), mode='bilinear', align_corners=True)
        return x

    def _freeze_bn(self):
        for m in self.modules():
            if isinstance(m, BatchNorm2d):
                m.eval()

    def _init_weight(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                n = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
                m.weight.data.normal_(0, math.sqrt(2. / n))
            elif isinstance(m, BatchNorm2d):
                m.weight.data.fill_(1)
                m.bias.data.zero_()


def Deeplabv3plus_res18(num_classes=3, downsample_factor=16, pretrained=False):
    resnet18 = ResNet(BasicBlock, [2, 2, 2, 2], downsample_factor)
    if pretrained:
        pretrain_dict = model_zoo.load_url(model_urls['resnet18'])
        model_dict = {}
        state_dict = resnet18.state_dict()
        for k, v in pretrain_dict.items():
            if k in state_dict:
                model_dict[k] = v
        state_dict.update(model_dict)
        resnet18.load_state_dict(state_dict)
    model = Deeplabv3plus(backbone=resnet18, num_classes=num_classes, os=downsample_factor, freeze_bn=False)
    return model


